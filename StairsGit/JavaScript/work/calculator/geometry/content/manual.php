<p><b>Особенности построения верхнего марша КО при узком проеме:</b><br/>
<ul>
<li>Если ширина проема А меньше чем M + 120мм для КО по конструктивным
соображениям невозможно спроектировать лестницу. В этом случае выдается ошибка и предложение
изменить ширину марша или проема.</li>
<li>При ширине проема от M+120 до M+220 продлевается верхняя забежная ступень или площадка</li>
<li>При ширине проема от M+220 до M+420 в верхнем марше одна ступень. Лестница вписывается в проем
за счет изменения верхней забежной ступени</li>
<li>При ширине проема более М+420 верхний марш строится обычным способом</li>
</ul>

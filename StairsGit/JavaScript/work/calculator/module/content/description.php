<p>Классическая лестница на надежном металлическом каркасе с деревянными ступенями. 
Каркас представляет собой два металлических косоура из толстого стального листа, 
сверху на которые укладываются ступени и подступенки. Снизу и сбоку каркас обшивается 
гипсокартоном. Лестница изготавливается на заказ под Ваш размер проема. Лестница отличается 
колоссальной прочностью, долговечностью и строгим классическим дизайном. Предназначена для 
установки внутри помещения в частных домах.
</p>
<h2>Особенности модели</h2>
<ul class="galka">
	<li>Надежный стальной каркас выдерживает нагрузку до 500 кг;</li>
	<li>Собирается без сварки за 2 часа</li>
	<li>Каркас полностью обшивается со всех сторон гипсокартоном;</li>
	<li>Лестница идеально подойдет Вам, так как изготавливается на заказ под Ваш размер;</li>
	<li>Лестница изготавливается на автоматическом оборудовании, что обеспечивает высочайшую точность и качество всех деталей;
</li>
	<li>Продуманная конструкция позволяет собрать и установить лестницу на объекте за 3-4 часа без использования сварки;
</li>
</ul>

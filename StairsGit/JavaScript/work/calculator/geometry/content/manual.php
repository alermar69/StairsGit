<h3>Инструкции по пользованию программой:</h3>
<h4>Для расчета нажмите на надпись Результаты расчета</h4>
<ol>
<li>Подъем ступени на верхнем забеге принимается равным подъему ступени верхнего марша;</li>
<li>Подъем ступени на нижнем забеге принимается равным подъему ступени среднего марша;</li>
</ol>


<p><b>Особенности построения верхнего марша ЛТ при узком проеме:</b><br/>
<ul>
<li>Если ширина проема А меньше чем M + 110мм для ЛТ по конструктивным
соображениям невозможно спроектировать лестницу. В этом случае выдается ошибка и предложение
изменить ширину марша или проема.</li>
<li>При ширине проема от M+110 до M+200 продлевается верхняя забежная ступень или площадка</li>
<li>При ширине проема от M+200 до M+400 в верхнем марше одна ступень. Лестница вписывается в проем
за счет изменения верхней забежной ступени</li>
<li>При ширине проема более М+400 в верхнем марше более 2 ступеней и лестница строится обычным способом</li>
</ul>
